/**
 * Простая (2 балла)
 *
 * Найти число Фибоначчи из ряда 1, 1, 2, 3, 5, 8, 13, 21, ... с номером n.
 * Ряд Фибоначчи определён следующим образом: fib(1) = 1, fib(2) = 1, fib(n+2) = fib(n) + fib(n+1)
 */
fun fib(n: Int): Int {
    var f1 = 1
    var f2 = 1
    var fn = 0
    var currentNumber = 3
    if ((n == 1) || (n == 2)) return 1 else {
        while (currentNumber <= n) {
            fn = f1 + f2
            f1 = f2
            f2 = fn
            currentNumber++
        }
    }
    return fn
}

